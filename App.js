var song1 = document.getElementById("Song1");
var icon1 = document.getElementById("icon1");

icon1.onclick = function () {
  if (song1.paused) {
    song1.play();
    icon1.src="media/pause.png";
  } else {
    song1.pause();
    icon1.src="media/icon.png";
  }
};

